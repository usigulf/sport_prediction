"""Pinned backend dependencies (P1-005)."""
import re
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[2]
BACKEND = REPO_ROOT / "backend"

DIRECT_DEPS = [
    "fastapi",
    "starlette",
    "uvicorn",
    "sqlalchemy",
    "psycopg2-binary",
    "redis",
    "pydantic",
    "pydantic-settings",
    "email-validator",
    "python-jose",
    "bcrypt",
    "python-multipart",
    "alembic",
    "python-dotenv",
    "pandas",
    "scikit-learn",
    "pillow",
    "stripe",
    "sentry-sdk",
]


def _package_lines(path: Path) -> dict[str, str]:
    versions: dict[str, str] = {}
    for line in path.read_text(encoding="utf-8").splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        match = re.match(r"^([A-Za-z0-9_.-]+)==([^;\s]+)", stripped)
        if match:
            versions[match.group(1).lower()] = match.group(2)
    return versions


def test_requirements_in_and_compile_script_exist():
    assert (BACKEND / "requirements.in").is_file()
    assert (BACKEND / "requirements-dev.in").is_file()
    assert (REPO_ROOT / "scripts" / "compile_requirements.sh").is_file()


def test_requirements_txt_is_fully_pinned():
    text = (BACKEND / "requirements.txt").read_text(encoding="utf-8")
    assert "autogenerated by uv" in text
    for line in text.splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        assert "==" in stripped, f"unpinned line: {stripped}"
        assert ">=" not in stripped, f"loose pin remains: {stripped}"


def test_direct_dependencies_are_pinned_in_requirements_txt():
    pinned = _package_lines(BACKEND / "requirements.txt")
    for name in DIRECT_DEPS:
        assert name in pinned, f"missing pinned {name}"


def test_audited_packages_meet_security_floors():
    pinned = _package_lines(BACKEND / "requirements.txt")
    assert pinned["python-multipart"] >= "0.0.31"
    assert pinned["python-dotenv"] >= "1.2.2"
    assert pinned["pillow"] >= "12.2.0"
    assert pinned["starlette"] >= "1.3.1"


def test_requirements_dev_txt_is_pinned_and_constrains_prod():
    text = (BACKEND / "requirements-dev.txt").read_text(encoding="utf-8")
    assert "-c requirements.txt" in (BACKEND / "requirements-dev.in").read_text(encoding="utf-8")
    for line in text.splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        assert "==" in stripped, f"unpinned dev line: {stripped}"
